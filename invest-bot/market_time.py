"""market_time.py — единое «московское» время для торгового дня.

Бот работает с MOEX через брокера (Tinkoff): календарный ТОРГОВЫЙ день, окна
данных ОИ/tradestats, каденс перекалибровки — всё считается по Москве, а не по
UTC и не по локальной зоне сервера. Иначе на стыке полуночи (когда UTC-дата и
локальная дата сервера расходятся) «сегодня» в разных ветках кода означало бы
разные календарные дни: OI/tradestats-снэпшот мог бы попасть не в тот торговый
день, окно аналитики — сдвинуться, дневной аккумулятор — сброситься не вовремя.

В РФ нет перехода на летнее время с 2011 года → MSK = фиксированный UTC+3,
DST-таблицы не нужны.
"""
from datetime import date, datetime, timedelta, timezone

MSK = timezone(timedelta(hours=3))


def now_msk() -> datetime:
    """Текущее московское время (timezone-aware)."""
    return datetime.now(MSK)


def today_msk() -> date:
    """Календарная МОСКОВСКАЯ дата — торговый день MOEX."""
    return now_msk().date()
